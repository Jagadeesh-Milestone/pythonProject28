#binary numbers:
#binary numbers starts with 0b
#base value is 2
#possible values are 0,1
a=7
print(bin(a))

b=0b111
print(b)

#hexa decimal numbers
#they starts with 0x
#base value is 16
#possible values are 0-9 and a-f
c=16
print(hex(c))

d=0xf
print(d)

#octal numbers:
#they starts with 0o
#base value is 8
#possible values are 0-7
e=10
print(oct(e))

f=0o10
print(f)