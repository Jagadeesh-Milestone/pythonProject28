#binary numbers:
#they starts with 0b
#base value is 2
#possible values are 0,1
a=10
print(bin(a))

b=0b1010
print(b)

#hexa decimal numbers:
#they starts with 0x
#base value is 16
#possible values are 0-9 and a-f
d=200
print(hex(d))

e=0xc8
print(e)

#octal numbers
#they starts with 0o
#base value is 8
#possible values are 0-7
f=19
print(oct(f))

g=0o23
print(g)