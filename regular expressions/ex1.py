#regular expressions
#a regular expression is a sequence of characters that forms a
#search pattern.
#it is used to check if a string contains the specified pattern.

#re module.
#python has a builtin package called re,it is used to work with the
#regular expressions.

#RegEx functions:
#regex provides a set of functions that allows you to search a
#string for a match.
#findall--it returns a list containing all the matches.
#search--it returns a match object if there is a match anywhere in the string
#split--it returns a list where the string has to be split at each match
#sub--it replaces one or more matches in a string.




