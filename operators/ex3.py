#logical operators
#logical and
# it returns true if both the statements are true
print(True and True)
print(True and False)
print(False and True)
print(False and False)
a=5
b=10
print(a<b and b>a)
print(a<b and b<a)
print(a>b and b>a)
print(a>b and b<a)

#logical or
#it returns true if anyone of the statement is true
print(True or True)
print(True or False)
print(False or True)
print(False or False)
a=5
b=10
print(a<b or b>a)
print(a<b or b<a)
print(a>b or b>a)
print(a>b or b<a)

#logical not
#it returns opposite to the given statement
print(not True)
print(not False)